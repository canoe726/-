/**
 * @param {number[]} nums
 * @return {number[][]}
 */
var threeSum = function (nums) {
  nums.sort((a, b) => a - b);

  let answer = [];

  for (let p1 = 0; p1 < nums.length - 2; p1++) {
    if (p1 > 0 && nums[p1] === nums[p1 - 1]) {
      continue;
    }
    let p2 = p1 + 1;
    let p3 = nums.length - 1;

    while (p2 < p3) {
      if (nums[p1] + nums[p2] + nums[p3] > 0) {
        p3 -= 1;
      } else if (nums[p1] + nums[p2] + nums[p3] === 0) {
        answer.push([nums[p1], nums[p2], nums[p3]]);
        p2 += 1;

        while (nums[p2] === nums[p2 - 1] && p2 < p3) {
          p2 += 1;
        }
      } else {
        p2 += 1;
      }
    }
  }

  return answer;
};

