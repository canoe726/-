/**
 * @param {number[]} nums
 * @return {number[][]}
 */
var threeSum = function (nums) {
  nums.sort((a, b) => a - b);

  const keys = {};
  let answer = [];

  for (let p1 = 0; p1 < nums.length - 2; p1++) {
    let p2 = p1 + 1;
    let p3 = nums.length - 1;

    while (p2 < p3) {
      if (nums[p1] + nums[p2] + nums[p3] >= 0) {
        if (nums[p1] + nums[p2] + nums[p3] === 0) {
          const candidate = [nums[p1], nums[p2], nums[p3]];

          if (!keys[candidate]) {
            keys[candidate] = true;
            answer.push(candidate);
          }
        }
        p3 -= 1;
      } else {
        p2 += 1;
      }
    }
  }

  return answer;
};

